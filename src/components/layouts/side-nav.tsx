function iconForKey(key: NavKey) {
  switch (key) {
    case 'dashboard':
      return <DashIcon />
    case 'initiatives':
      return <InitIcon />
    case 'tasks':
      return <TaskIcon />
    case 'bureau':
      return <BureauIcon />
    case 'congress':
      return <CongressIcon />
    case 'resources':
      return <ResourceIcon />
    case 'partners':
      return <PartnerIcon />
    case 'profile':
      return <ProfileIcon />
    case 'admin':
      return <AdminIcon />
    default:
      return <BoardIcon />
  }
}

interface SideNavProps {
  role: string
  actualRole?: string
}

export function SideNav({ role, actualRole }: SideNavProps) {
  const pathname = usePathname()
  let items = getSideNavItems(role)

  // Always show admin link when actual role is PlatformAdmin (even in view-as mode)
  if (actualRole === 'PlatformAdmin' && !items.some((i) => i.key === 'admin')) {
    items = [...items, { key: 'admin', label: 'User Management', href: '/app/admin/users' }]
  }

  return (
    <aside
      className="hidden w-56 shrink-0 border-r border-neutral-200 bg-white lg:flex lg:flex-col"
      role="complementary"
      aria-label="Sidebar navigation"
    >
      <nav className="flex flex-col gap-0.5 p-3 pt-4" aria-label="Main navigation">
        {items.map((item) => {
          const active = pathname === item.href || pathname.startsWith(item.href + '/')
          return (
            <Link
              key={item.href}
              href={item.href}
              className={[
                'flex items-center gap-2.5 rounded-lg px-2.5 py-2 text-sm font-medium transition-colors',
                active
                  ? 'bg-orange-50 text-orange-700 border-l-2 border-orange-600'
                  : 'text-neutral-600 hover:bg-neutral-100 hover:text-neutral-900',
              ].join(' ')}
            >
              {iconForKey(item.key)}
              {item.label}
            </Link>
          )
        })}
      </nav>
    </aside>
  )
}
